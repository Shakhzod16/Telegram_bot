LANGUAGE_CODES = ("uz", "ru", "en")

STATUS_TEXT_KEYS = {
    "CREATED": "status_created",
    "CONFIRMED": "status_confirmed",
    "IN_PROGRESS": "status_in_progress",
    "DELIVERING": "status_delivering",
    "DELIVERED": "status_delivered",
    "PAID": "status_paid",
    "CANCELLED": "status_cancelled",
}


def t(key: str, language: str, **kwargs: str) -> str:
    translations = TEXTS[key]
    template = translations.get(language) or translations["en"]
    return template.format(**kwargs)


def status_text(status: str, language: str) -> str:
    key = STATUS_TEXT_KEYS.get(status.upper(), "status_unknown")
    return t(key, language)


def frontend_texts(language: str) -> dict[str, str]:
    return {key: t(key, language) for key in TEXTS if key.startswith("frontend_")}
