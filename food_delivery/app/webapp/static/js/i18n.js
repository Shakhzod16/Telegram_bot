function normalizeLang(value) {
  const lang = String(value || "").toLowerCase();
  if (lang === "ru" || lang === "en") return lang;
  return "uz";
}

function translateText(lang, key, fallback, params) {
  const safeLang = normalizeLang(lang);
  let text = (FE_TRANSLATIONS[safeLang] && FE_TRANSLATIONS[safeLang][key]) || fallback || key;
  if (!params) return text;
  Object.keys(params).forEach(function (name) {
    text = text.replace(new RegExp(`\\{${name}\\}`, "g"), String(params[name]));
  });
  return text;
}

function applyI18nDom(lang) {
  const safeLang = normalizeLang(lang);
  document.documentElement.lang = safeLang;

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    const key = el.getAttribute("data-i18n");
    if (!key) return;
    const fallback = el.getAttribute("data-i18n-fallback") || el.textContent || "";
    el.textContent = translateText(safeLang, key, fallback);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
    const key = el.getAttribute("data-i18n-placeholder");
    if (!key) return;
    const fallback = el.getAttribute("placeholder") || "";
    el.setAttribute("placeholder", translateText(safeLang, key, fallback));
  });
}

async function loadLanguageFromBackend() {
  const fromStorage = normalizeLang(localStorage.getItem("fe_lang") || "uz");

  try {
    await (window.authReady || Promise.resolve());
    if (!window.apiFetch) return fromStorage;
    const me = await window.apiFetch("/api/v1/users/me");
    const lang = normalizeLang(me && me.language ? me.language : fromStorage);
    localStorage.setItem("fe_lang", lang);
    return lang;
  } catch (_) {
    return fromStorage;
  }
}

window.FE_TRANSLATIONS = FE_TRANSLATIONS;
window.feT = function feT(key, fallback, params) {
  return translateText(window.__appLang || "uz", key, fallback, params);
};

window.setAppLanguage = function setAppLanguage(lang) {
  const safeLang = normalizeLang(lang);
  window.__appLang = safeLang;
  localStorage.setItem("fe_lang", safeLang);
  applyI18nDom(safeLang);
  document.dispatchEvent(new CustomEvent("fe:lang-changed", { detail: { lang: safeLang } }));
  return safeLang;
};

window.getAppLanguage = function getAppLanguage() {
  return normalizeLang(window.__appLang || localStorage.getItem("fe_lang") || "uz");
};

window.appLangReady = (async function () {
  const lang = await loadLanguageFromBackend();
  return window.setAppLanguage(lang);
})();
